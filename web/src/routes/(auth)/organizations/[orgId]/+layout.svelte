<script lang="ts">
	import AiWindow from '$lib/components/AIWindow/AIWindow.svelte';
	import Header from '$lib/components/Header/Header.svelte';
	import Sidebar from '$lib/components/Sidebar/Sidebar.svelte';
	import Chat from '$lib/ai/svelte/components/chat/Chat.svelte';
	import { ConversationStore } from '$lib/ai/svelte/store/conversationStore';
	import {
		URL_API_SIGNOUT,
		URL_DASHBOARD,
		URL_ORGANIZATIONS,
		urlOrganization,
		urlOrganizationMembers,
		urlOrganizationFiles,
		urlOrganizationSubscription
	} from '$lib/url';
	import { Cog, Building2, Users, CreditCard, Files } from '@lucide/svelte';
	import SidebarLink from '$lib/components/Sidebar/SidebarLink.svelte';
	import Button from '$lib/components/ui/button/button.svelte';
	let { children, data } = $props();
	const user = $derived(data.user);
	const organization = $derived(data.organization);
	const orgId = $derived(organization.id);

	const onSignOut = async () => {
		const res = await fetch(URL_API_SIGNOUT, {
			method: 'POST'
		});
		if (res.ok) {
			window.location.reload();
		}
	};

	const { generating, conversation, sendMessage } = new ConversationStore({
		handleAiFunctionCall: () => console.log
	});

	const items = $derived($conversation);
	const handleSendMessage = async (message: string) => {
		await sendMessage(`/api/v1/organizations/${orgId}/handle-turn`, message);
	};
</script>

<div class="flex h-screen grow">
	<div class="flex h-screen min-h-screen flex-1 flex-col bg-gray-50">
		<Header {user} {onSignOut}>
			{#snippet contextItems()}
				<Button
					variant="ghost"
					class="block w-full px-4 text-left"
					role="menuitem"
					tabindex={-1}
					href={URL_ORGANIZATIONS}
				>
					Switch Organization
				</Button>
				<Button
					variant="ghost"
					class="block w-full px-4 text-left"
					role="menuitem"
					tabindex={-1}
					href={URL_DASHBOARD}
				>
					Switch to Dashboard View
				</Button>
			{/snippet}
		</Header>
		<div class="flex grow">
			<Sidebar>
				{#snippet items(isExpanded)}
					<SidebarLink
						{isExpanded}
						href={urlOrganization(orgId)}
						label={organization.name}
						Icon={Building2}
					/>
					<SidebarLink
						{isExpanded}
						href={urlOrganizationMembers(orgId)}
						label="Members"
						Icon={Users}
					/>
					<SidebarLink
						{isExpanded}
						href={urlOrganizationFiles(orgId)}
						label="Files"
						Icon={Files}
					/>
					<SidebarLink
						{isExpanded}
						href={urlOrganizationSubscription(orgId)}
						label="Subscription"
						Icon={CreditCard}
					/>
				{/snippet}
			</Sidebar>
			<main class="flex w-full flex-1 px-4 py-8 sm:px-6 lg:px-8">
				{@render children()}
			</main>
		</div>
	</div>
	<AiWindow>
		<Chat onSendMessage={handleSendMessage} generating={$generating} {items} />
	</AiWindow>
</div>
